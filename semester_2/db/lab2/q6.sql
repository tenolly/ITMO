SELECT Н_УЧЕНИКИ.ГРУППА, Н_УЧЕНИКИ.ИД, Н_ЛЮДИ.ФАМИЛИЯ, Н_ЛЮДИ.ИМЯ, Н_ЛЮДИ.ОТЧЕСТВО, Н_УЧЕНИКИ.СОСТОЯНИЕ, Н_УЧЕНИКИ.ПРИМЕЧАНИЕ FROM Н_УЧЕНИКИ
RIGHT JOIN Н_ЛЮДИ ON Н_ЛЮДИ.ИД = Н_УЧЕНИКИ.ЧЛВК_ИД
WHERE EXISTS (
    SELECT 1 FROM Н_ПЛАНЫ
    RIGHT JOIN Н_ФОРМЫ_ОБУЧЕНИЯ ON Н_ПЛАНЫ.ФО_ИД = Н_ФОРМЫ_ОБУЧЕНИЯ.ИД 
    WHERE (Н_ФОРМЫ_ОБУЧЕНИЯ.НАИМЕНОВАНИЕ = 'Заочная' OR Н_ФОРМЫ_ОБУЧЕНИЯ.НАИМЕНОВАНИЕ = 'Очная') AND Н_УЧЕНИКИ.ПЛАН_ИД = Н_ПЛАНЫ.ИД
) AND Н_УЧЕНИКИ.НАЧАЛО = '2012.09.01';
